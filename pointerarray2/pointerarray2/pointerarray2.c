#include<stdio.h>
void main() {
	static int x[] = { 1,2,3,4 }; //1차원 배열 선언과 초기회
	static int y[] = { 10,20,30,40 }; //1차원 배열 선언과 초기회
	int i, z[4];
	for (i = 0; i < 4; i++)
		z[i] = x[i] + y[3 - i];
	printf("반대 위치의 배열요소의 합\n");
	for (i = 0; i < 4; i++)
		printf("%d+%d=%d\n", x[i], y[3 - i], z[i]);

	fgetc(stdin);
}